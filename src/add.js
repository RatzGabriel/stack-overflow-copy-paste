export default add

/**
 * Thiss method will perform addition operation.
 *
 * @param {Number} num1 - first number for addition
 * @param {Number} num2 - second  number for additon
 * @return {Number} - Result of addition
 */
function add(num1, num2) {
  const a = num1
  const be = num2

  return a + be
}
